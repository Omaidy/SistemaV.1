    private void jButtonIniActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonIniActionPerformed
    String nombreUsuario = txt_usuario.getText();//
    String contrasena = txt_contrasena.getText();//
    
    // Verificar las credenciales del usuario
    boolean inicioSesionExitoso = loginService != null && loginService.verificarInicioSesion(nombreUsuario, contrasena);
    
    // Comprobar si el inicio de sesión fue exitoso
    if (inicioSesionExitoso) {
        // Si las credenciales son válidas, muestra un mensaje de éxito
        JOptionPane.showMessageDialog(this, "Inicio de sesión exitoso", "Éxito", JOptionPane.INFORMATION_MESSAGE);
        // Aquí podrías redirigir a otra ventana o realizar otras acciones después del inicio de sesión
    } else {
        // Si las credenciales son inválidas, muestra un mensaje de error
        JOptionPane.showMessageDialog(this, "Usuario o contraseña incorrectos", "Error", JOptionPane.ERROR_MESSAGE);
    }
    }//GEN-LAST:event_jButtonIniActionPerformed

