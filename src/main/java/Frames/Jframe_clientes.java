    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
    int dni = Integer.parseInt(txtDNI.getText());
    
    try {
        // Buscar el empleado en la base de datos
        Cliente cliente = clienteDAO.buscarPorDni(dni);
        
        if (cliente != null) {
            // Mostrar la información del empleado en los campos del formulario
            txtTipo.setText(cliente.getTipo());
            txtNombres.setText(cliente.getNombre());
            txtApellidos.setText(cliente.getApellidos());
            txtCorreo.setText(cliente.getCorreo());
            txtTelefono.setText(String.valueOf(cliente.getNumero()));
            txtDireccion.setText(cliente.getDireccion());
        } else {
            // Manejar el caso en que no se encuentre el empleado
            JOptionPane.showMessageDialog(null, "Cliente no encontrado", "Información", JOptionPane.INFORMATION_MESSAGE);
        }
    } catch (SQLException ex) {
        // Manejar la excepción apropiadamente
        ex.printStackTrace();
        JOptionPane.showMessageDialog(null, "Error al buscar el cliente: " + ex.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
    }
    }//GEN-LAST:event_btnBuscarActionPerformed

